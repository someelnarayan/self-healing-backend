def restart_service(
    target_name: str,
    config: AppConfig,
) -> ActionResult:
    """
    Used mainly for local/docker targets such as bookshop.
    Restarts a configured Docker container and optionally
    verifies health_url recovery.
    """
    start = time.time()

    target = _get_target(target_name, config)
    if not target:
        return ActionResult(
            False,
            0,
            f"Unknown target: {target_name}",
        )

    try:
        if not target.container_name:
            return ActionResult(
                False,
                0,
                f"Target {target_name} has no container_name configured",
            )

        client = docker.from_env()
        container = client.containers.get(
            target.container_name
        )

        print(
            f"[Executor] Restarting container {target.container_name}",
            flush=True,
        )

        container.restart()

        # Optional health verification
        if target.health_url:
            for _ in range(30):
                time.sleep(1)

                try:
                    response = httpx.get(
                        target.health_url,
                        timeout=3,
                    )

                    if response.status_code == 200:
                        duration = int((time.time() - start) * 1000)
                        return ActionResult(True, duration)

                except Exception:
                    pass

            duration = int((time.time() - start) * 1000)
            return ActionResult(
                False,
                duration,
                "Health endpoint did not recover after restart",
            )

        duration = int((time.time() - start) * 1000)
        return ActionResult(True, duration)

    except Exception as e:
        duration = int((time.time() - start) * 1000)
        return ActionResult(False, duration, str(e))


def retry_http_endpoint(
    target_name: str,
    config: AppConfig,
) -> ActionResult:
    """
    Lightweight recovery / verification action:
    retries the health endpoint a few times.
    """
    start = time.time()

    target = _get_target(target_name, config)
    if not target:
        return ActionResult(
            False,
            0,
            f"Unknown target: {target_name}",
        )

    if not target.health_url:
        return ActionResult(
            False,
            0,
            f"Target {target_name} has no health_url configured",
        )

    delay = 1

    for _ in range(3):
        try:
            response = httpx.get(
                target.health_url,
                timeout=5,
            )

            if response.status_code == 200:
                duration = int((time.time() - start) * 1000)
                return ActionResult(True, duration)

        except Exception:
            pass

        time.sleep(delay)
        delay *= 2

    duration = int((time.time() - start) * 1000)
    return ActionResult(False, duration, "Endpoint retry failed")


def send_alert(
    target_name: str,
    config: AppConfig,
    message: str = "",
) -> ActionResult:
    """
    Sends alert via Slack/email if configured.
    Falls back to console alert if no channel configured.
    """
    start = time.time()
    sent = False

    if config.alerting.slack_webhook_url:
        try:
            payload = {
                "text": f"[Self-Healer] {target_name}\n{message}"
            }

            httpx.post(
                config.alerting.slack_webhook_url,
                json=payload,
                timeout=5,
            )
            sent = True

        except Exception as e:
            print(
                f"[Executor] Slack alert failed: {e}",
                flush=True,
            )

    if config.alerting.smtp_host and config.alerting.alert_email_to:
        try:
            msg = MIMEText(message)
            msg["Subject"] = f"[Self-Healer] Alert - {target_name}"
            msg["From"] = config.alerting.smtp_user
            msg["To"] = config.alerting.alert_email_to

            with smtplib.SMTP(
                config.alerting.smtp_host,
                config.alerting.smtp_port,
            ) as server:
                server.starttls()

                if config.alerting.smtp_user:
                    server.login(
                        config.alerting.smtp_user,
                        config.alerting.smtp_pass,
                    )

                server.send_message(msg)

            sent = True

        except Exception as e:
            print(
                f"[Executor] Email alert failed: {e}",
                flush=True,
            )

    if not sent:
        print(
            f"[ALERT] {target_name}: {message}",
            flush=True,
        )

    duration = int((time.time() - start) * 1000)
    return ActionResult(True, duration)


def rotate_log(
    target_name: str,
    config: AppConfig,
) -> ActionResult:
    """
    Local log rotation:
    renames current log file to a timestamped backup
    and creates a fresh empty log file.
    """
    start = time.time()

    target = _get_target(target_name, config)
    if not target:
        return ActionResult(
            False,
            0,
            f"Unknown target: {target_name}",
        )

    log_path = target.log_path

    if not log_path:
        return ActionResult(
            False,
            0,
            f"Target {target_name} has no log_path configured",
        )

    try:
        backup_file = f"{log_path}.bak.{int(time.time())}"

        os.rename(log_path, backup_file)
        open(log_path, "w").close()

        duration = int((time.time() - start) * 1000)
        return ActionResult(True, duration)

    except Exception as e:
        duration = int((time.time() - start) * 1000)
        return ActionResult(False, duration, str(e))


def restart_process(
    target_name: str,
    config: AppConfig,
) -> ActionResult:
    """
    SSH demo healing action:
    runs the configured restart_command on the remote WSL demo target.

    Example:
        bash /home/hpsomeel/healer_demo_lab/restart_dummy.sh
    """
    start = time.time()

    target = _get_target(target_name, config)
    if not target:
        return ActionResult(
            False,
            0,
            f"Unknown target: {target_name}",
        )

    if not target.is_ssh:
        return ActionResult(
            False,
            0,
            f"restart_process only supports SSH targets, got {target.type}",
        )

    if not target.restart_command:
        return ActionResult(
            False,
            0,
            f"Target {target_name} has no restart_command configured",
        )

    print(
        f"[Executor] SSH restart_process on {target.name}: {target.restart_command}",
        flush=True,
    )

    ok, output = _ssh_run_command(
        target,
        target.restart_command,
    )

    duration = int((time.time() - start) * 1000)

    if not ok:
        return ActionResult(
            False,
            duration,
            output or "restart_process failed",
        )

    # Optional verification
    if target.process_name:
        verify_cmd = (
            f"pgrep -f '{target.process_name}' >/dev/null && "
            f"echo RUNNING || echo STOPPED"
        )

        verify_ok, verify_output = _ssh_run_command(
            target,
            verify_cmd,
        )

        if not verify_ok or verify_output.strip() != "RUNNING":
            return ActionResult(
                False,
                duration,
                f"Restart command ran but process verification failed: {verify_output}",
            )

    return ActionResult(True, duration)


