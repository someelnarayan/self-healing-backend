class Executor:
    def __init__(
        self,
        config: AppConfig,
        kb: KnowledgeBase,
    ):
        self.config = config
        self.kb = kb

    def execute(
        self,
        plan: ActionPlan,
    ) -> list[ActionResult]:

        results: list[ActionResult] = []
        anomaly = plan.anomaly

        self.kb.update_incident_status(
            anomaly.target_name,
            anomaly.anomaly_type,
            "IN_PROGRESS",
        )

        alert_message = (
            f"Anomaly: {anomaly.anomaly_type}\n"
            f"Severity: {anomaly.severity}\n"
            f"Target: {anomaly.target_name}\n"
            f"Value: {anomaly.metric_value}\n"
            f"Context: {anomaly.context}"
        )

        overall_success = True

        for action_name in plan.actions:
            action_fn = ACTION_REGISTRY.get(action_name)

            if not action_fn:
                print(
                    f"[Executor] Unknown action: {action_name}",
                    flush=True,
                )

                result = ActionResult(
                    False,
                    0,
                    f"Unknown action: {action_name}",
                )

                self.kb.write_audit(
                    target_name=anomaly.target_name,
                    anomaly_type=anomaly.anomaly_type,
                    action=action_name,
                    success=False,
                    duration_ms=0,
                    error_msg=result.error,
                )

                results.append(result)
                overall_success = False
                continue

            print(
                f"[Executor] Running {action_name} on {anomaly.target_name}",
                flush=True,
            )

            try:
                if action_name == "send_alert":
                    result = action_fn(
                        anomaly.target_name,
                        self.config,
                        alert_message,
                    )
                else:
                    result = action_fn(
                        anomaly.target_name,
                        self.config,
                    )

            except Exception as e:
                result = ActionResult(
                    False,
                    0,
                    str(e),
                )

            self.kb.write_audit(
                target_name=anomaly.target_name,
                anomaly_type=anomaly.anomaly_type,
                action=action_name,
                success=result.success,
                duration_ms=result.duration_ms,
                error_msg=result.error,
            )

            results.append(result)

            if not result.success:
                overall_success = False

            print(
                f"[Executor] {action_name} -> "
                f"{'SUCCESS' if result.success else 'FAILED'} "
                f"({result.duration_ms} ms)",
                flush=True,
            )

        # --------------------------------------------------------
        # Final incident / cooldown handling
        # --------------------------------------------------------
        if overall_success:
            self.kb.update_incident_status(
                anomaly.target_name,
                anomaly.anomaly_type,
                "RESOLVED",
            )

            self.kb.resolve_incident(
                anomaly.target_name,
                anomaly.anomaly_type,
            )

            cooldown_key = plan.metadata.get(
                "cooldown_key",
                f"{anomaly.target_name}:{anomaly.anomaly_type}",
            )

            rule = self.config.rule_for(anomaly.anomaly_type)
            cooldown_minutes = rule.cooldown_minutes if rule else 1
            cooldown_seconds = max(1, cooldown_minutes * 60)

            self.kb.set_cooldown(
                cooldown_key,
                cooldown_seconds,
            )

        else:
            self.kb.update_incident_status(
                anomaly.target_name,
                anomaly.anomaly_type,
                "FAILED",
            )

        return results