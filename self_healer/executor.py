class ActionResult:
    def __init__(
        self,
        success: bool,
        duration_ms: int,
        error: Optional[str] = None,
    ):
        self.success = success
        self.duration_ms = duration_ms
        self.error = error


# ------------------------------------------------------------
# Helper
# ------------------------------------------------------------

def _get_target(
    target_name: str,
    config: AppConfig,
) -> Optional[Target]:
    return next(
        (
            t
            for t in config.targets
            if t.name == target_name or t.id == target_name
        ),
        None,
    )


def _ssh_run_command(
    target: Target,
    command: str,
) -> tuple[bool, str]:
    """
    Runs a command on an SSH target and returns:
    (success, combined_output)
    """
    client = paramiko.SSHClient()
    client.set_missing_host_key_policy(
        paramiko.AutoAddPolicy()
    )

    try:
        client.connect(
            hostname=target.host,
            username=target.username,
            key_filename=target.ssh_key,
            timeout=10,
        )

        stdin, stdout, stderr = client.exec_command(command)

        out = stdout.read().decode().strip()
        err = stderr.read().decode().strip()

        combined = "\n".join(
            x for x in [out, err] if x
        ).strip()

        exit_code = stdout.channel.recv_exit_status()

        return exit_code == 0, combined

    except Exception as e:
        return False, str(e)

    finally:
        client.close()


